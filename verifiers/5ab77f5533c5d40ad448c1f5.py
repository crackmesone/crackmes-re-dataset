# CrackmeVS6 by netstavi - reverse engineered serial validator
# Based on the writeup by kao
#
# The algorithm:
# 1. The crackme generates a serial number based on drive C serial and current hour
#    (serial generation is described as 'too long to reproduce' - ASSUMPTION: we skip keygen for serial)
# 2. The unlock code is validated as follows:
#    a. Take the full string "Serial No. : <serial>" and sum all ASCII values -> sum_serial
#    b. Take the full string "User name: <name>" and sum all ASCII values -> sum_name
#    c. Multiply: product = sum_serial * sum_name
#    d. Get current hour (0-23) and multiply: result = product * current_hour
#    e. Convert result to string, take leftmost 6 characters -> this is the unlock code
#
# ASSUMPTION: The serial number field format is exactly "Serial No. : XXXXXXXXXX-YYYYYYYYY"
#             (generated by the crackme itself based on drive C serial + current hour)
# ASSUMPTION: The name field format is exactly "User name: <name>"
# ASSUMPTION: We cannot fully replicate keygen without knowing the drive C serial generation
#             algorithm (described as too long/boring to reproduce in the writeup)

from datetime import datetime


def sum_ascii(s: str) -> int:
    """Sum ASCII values of all characters in the string."""
    return sum(ord(c) for c in s)


def compute_unlock_code(serial_string: str, name_string: str, hour: int) -> str:
    """
    Compute the 6-character unlock code.
    
    serial_string: full string like 'Serial No. : XXXXXXXXXX-YYYYYYYYY'
    name_string:   full string like 'User name: SomeName'
    hour:          current hour in 24h system (0-23)
    """
    sum_serial = sum_ascii(serial_string)
    sum_name = sum_ascii(name_string)
    product = sum_serial * sum_name
    result = product * hour
    # Convert to string and take 6 leftmost chars
    unlock = str(result)[:6]
    return unlock


def verify(name: str, serial: str, unlock_code: str = None, hour: int = None) -> bool:
    """
    Verify if the unlock_code is correct for the given name and serial.
    
    name:         the user name (without prefix)
    serial:       the serial number string (without prefix, e.g. 'XXXXXXXXXX-YYYYYYYYY')
    unlock_code:  the 6-char code entered by the user
    hour:         hour to use (defaults to current hour)
    """
    if hour is None:
        hour = datetime.now().hour
    if unlock_code is None:
        return False
    
    # ASSUMPTION: exact label text used in the VB form
    serial_string = f"Serial No. : {serial}"
    name_string = f"User name: {name}"
    
    expected = compute_unlock_code(serial_string, name_string, hour)
    return unlock_code == expected


def keygen(name: str, serial: str, hour: int = None) -> str:
    """
    Generate the unlock code for a given name and serial number.
    
    ASSUMPTION: The serial must come from the crackme itself (drive C serial + hour based).
    We cannot generate the serial here because that part of the algorithm was not
    reproduced in the writeup.
    """
    if hour is None:
        hour = datetime.now().hour
    
    # ASSUMPTION: exact label text used in the VB form
    serial_string = f"Serial No. : {serial}"
    name_string = f"User name: {name}"
    
    unlock = compute_unlock_code(serial_string, name_string, hour)
    return unlock




    def getdword(s, offset):
        padded = (s + '\x00' * 64).encode('latin-1')
        if offset + 4 <= len(padded):
            return struct.unpack_from('<I', padded, offset)[0]
        return 0

    # ASSUMPTION: arr[] is all zeros or some known constant table; we use zeros
    # ASSUMPTION: arr has 0x40/4 = 16 entries
    arr = [0] * 16  # ASSUMPTION: arr contents unknown

    # Copy IS_A_WHORE string into name buffer first (as seen in assembly)
    # The assembly copies IS_A_WHORE string over the name buffer before processing
    # ASSUMPTION: name is overwritten with IS_A_WHORE padded/repeated

def ror8(val, n):
    """Rotate right 8-bit value by n bits."""
    val &= 0xFF
    n &= 7
    return ((val >> n) | (val << (8 - n))) & 0xFF

def rol8(val, n):
    """Rotate left 8-bit value by n bits."""
    val &= 0xFF
    n &= 7
    return ((val << n) | (val >> (8 - n))) & 0xFF


def compute_key_from_winkey(win_key: bytes) -> bytes:
    """
    From keygen.c:
        for Counter in range(27):
            Key[Counter] = Key[Counter] + WinKey[Counter]
    Where Key starts as BASE_KEY (27 bytes) and WinKey is the Windows ProductKey.
    The serial is Key[Counter] + WinKey[Counter] for each position.
    """
    key = bytearray(BASE_KEY)
    for i in range(min(27, len(win_key))):
        key[i] = (key[i] + win_key[i]) & 0xFF
    return bytes(key)

def compute_serial_checksums(serial_bytes: bytes):
    """
    From keygen.txt, the crackme computes two checksums from the serial
    after the subtraction step.
    
    The 'Key' buffer passed into the checksum loop is:
        Key[Counter] = Key[Counter] - WinKey[Counter]
    which reverses the addition, so effectively Key = BASE_KEY (the original constant).
    
    Then checksums are computed over 257 bytes of that buffer.
    """
    cs = 0   # CheckSum
    cs1 = 0  # CheckSum1
    # ASSUMPTION: 'Key' here after subtraction equals BASE_KEY padded with zeros to 257 bytes
    buf = bytearray(serial_bytes[:256]).ljust(257, b'\x00')
    for i in range(257):
        b = buf[i]
        cs1 = (cs1 + b) & 0xFF
        cs1 = ror8(cs1, b)
        cs = ror8(cs, b)
        cs = (cs ^ b) & 0xFF
    return cs, cs1

def decrypt_byte(check_byte: int, checksum: int, checksum1: int, key_byte: int, file_pos: int) -> int:
    """
    Reconstructed from the Decrypt() function in the writeup (assembly -> Python).
    This is the per-byte decryption used to verify the DLL.
    """
    dl = check_byte & 0xFF
    dl = (dl ^ checksum) & 0xFF
    cl = checksum1 & 0xFF
    dl = ror8(dl, cl)

    ax = file_pos & 0xFFFF
    al = ax & 0xFF
    ah = (ax >> 8) & 0xFF
    bx = ax
    cx = ax
    bl = bx & 0xFF
    cl_fp = cx & 0xFF
    ch_fp = (cx >> 8) & 0xFF

    al_new = (al & ah) & 0xFF
    cl_new = (cl_fp + ch_fp) & 0xFF
    bl_new = ror8(bl, 3)

    dl = rol8(dl, cl_new)
    dl = (dl + cl_new) & 0xFF
    dl = (dl ^ bl_new) & 0xFF
    dl = (dl ^ al_new) & 0xFF
    # neg dl: two's complement negation
    dl = (-dl) & 0xFF
    dl = (dl ^ key_byte) & 0xFF
    return dl

def verify(name: str, serial: str) -> bool:
    """
    The crackme uses the Windows ProductKey from the registry as 'name' (unique ID).
    The serial is generated by: serial[i] = BASE_KEY[i] + win_key[i] for i in 0..26.
    
    Verification (from the Unlock button logic in keygen.txt):
        After entering serial, the program computes:
            key_buf[i] = serial[i] - win_key[i]
        which should recover BASE_KEY.
        Then checksums are computed and used to decrypt Coded.dll to TheKey.dll.
    
    We verify that serial[i] - name[i] == BASE_KEY[i] for each position (mod 256).
    ASSUMPTION: 'name' here is the Windows ProductKey string used as WinKey.
    """
    win_key = name.encode('ascii') if isinstance(name, str) else name
    serial_bytes = serial.encode('ascii') if isinstance(serial, str) else serial
    
    key_len = len(ASM_BASE_KEY)  # 27
    if len(serial_bytes) < key_len:
        return False
    
    for i in range(key_len):
        wk_byte = win_key[i] if i < len(win_key) else 0
        recovered = (serial_bytes[i] - wk_byte) & 0xFF
        if recovered != ASM_BASE_KEY[i]:
            return False
    return True

def keygen(name: str) -> str:
    """
    Generate serial for a given Windows ProductKey (name).
    serial[i] = BASE_KEY[i] + win_key[i]  (mod 256)
    """
    win_key = name.encode('ascii') if isinstance(name, str) else name
    serial = bytearray(len(ASM_BASE_KEY))
    for i in range(len(ASM_BASE_KEY)):
        wk = win_key[i] if i < len(win_key) else 0
        serial[i] = (ASM_BASE_KEY[i] + wk) & 0xFF
    return serial.decode('latin-1')



#   2) The 16 bytes are then used as an AES-128 key (with a hardcoded pseudo-random
#      plaintext derived from srand(0x12345678)) to decrypt a block.
#   3) The decrypted block's first 4 bytes must equal sub_401000(name, len(name))
#      i.e., a 32-bit hash of the name.
#
# The pseudo-random sequence (from srand(0x12345678), MSVC rand):
# ASSUMPTION: The PseudoRand values below are taken directly from the writeup.
# They serve as the plaintext block fed to AES decryption.

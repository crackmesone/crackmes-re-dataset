import random

def compute_values(username):
    """Compute the first XOR parameter from the username."""
    val1093 = 0
    val1094 = 0
    val1095 = 0
    val1096 = 0
    for ii in range(len(username)):
        val1094 = ord(username[ii]) ^ 0xFF
        tmp_val = val1093
        for jj in range(4):
            tmp_val = tmp_val & 0xFF
            remainder = 1 if tmp_val > 0x80 else 0
            tmp_val = tmp_val * 2 + remainder
        val1095 = tmp_val & 0xFF
        val1096 = (val1094 - val1095 + val1096) & 0xFF
        val1093 = ord(username[ii])
    return val1096

def to_ascii(value):
    """Convert a nibble value (0-15) to uppercase hex ASCII character."""
    if value < 0xA:
        return chr(value + 0x30)
    else:
        return chr(value + 0x37)

def to_string(in_list):
    """Convert a list of chars to uppercase hex string of their ASCII values."""
    dest = ''.join(in_list)
    result = ''.join('%x' % ord(c) for c in dest)
    return result.upper()

def extract_value(c):
    """Extract numeric value from a hex ASCII character."""
    val_ascii = ord(c)
    if val_ascii > 0x39:
        return val_ascii - 0x37
    else:
        return val_ascii - 0x30

def computes_second_xor_param(xored_value, first_xor_param):
    """Compute the second XOR parameter used as serial suffix."""
    temp_value = xored_value ^ first_xor_param
    for ii in range(4):
        remainder = 0
        temp_value = temp_value & 0xFF
        if temp_value > 0x80:
            remainder = 1
        temp_value = temp_value * 2 + remainder
    return temp_value & 0xFF

def keygen(username, xored_value=None):
    """Generate a valid serial for the given username."""
    if len(username) < 6:
        raise ValueError('Username must be at least 6 characters')

    # First XOR param derived from username
    first_xor_param = compute_values(username)

    # dest_string starts as 'LNSDF' (5 chars)
    dest_string = list('LNSDF')

    # Random value for xoring
    if xored_value is None:
        xored_value = random.randint(10, 0x7F)

    # XOR dest_string with username characters (cycling)
    counter = 0
    for ii in range(len(username)):
        if counter == len(dest_string):
            counter = 0
        dest_string[counter] = chr(ord(dest_string[counter]) ^ ord(username[ii]))
        counter += 1

    # Add xored_value to each char
    for ii in range(len(dest_string)):
        dest_string[ii] = chr(ord(dest_string[ii]) + xored_value)

    # Compute second XOR param and append
    second_xor_param = computes_second_xor_param(xored_value, first_xor_param)

    if second_xor_param <= 0xF:
        dest_string.append(chr(0))
    dest_string.append(chr(second_xor_param))

    # Convert to hex string
    dest_string2 = to_string(dest_string)

    # Extract nibble values
    string_num = [extract_value(c) for c in dest_string2]

    # Reconstruct serial from nibbles (bit packing)
    solution = ['0'] * 10

    # positions 1,2
    solution[0] = to_ascii(string_num[1])
    solution[1] = to_ascii((string_num[0] >> 1) + ((string_num[3] & 0x3) << 3))

    # positions 3,4
    solution[2] = to_ascii(0x4 * (string_num[2] & 0x7) + (string_num[3] >> 2))
    solution[3] = to_ascii(((string_num[2] & 0x8) >> 3) + ((string_num[5] & 0xF) << 1))

    # positions 5,6
    solution[4] = to_ascii(string_num[4] + ((string_num[7] & 0x1) << 0x4))
    solution[5] = to_ascii((string_num[7] >> 1) + ((string_num[6] & 0x3) << 0x3))

    # positions 7,8
    solution[6] = to_ascii(((string_num[6] & 0xC) >> 2) + ((string_num[9] & 0x7) << 2))
    solution[7] = to_ascii(((string_num[9] & 0x8) >> 3) + (string_num[8] << 1))

    # positions 9,A
    solution[8] = to_ascii(string_num[11] + (string_num[10] & 0x1) * 0x10)
    solution[9] = to_ascii((string_num[10] & 0xF) >> 1)

    serial = ''.join(solution)
    return serial


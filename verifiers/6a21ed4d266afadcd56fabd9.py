def _generate_raw(username: str) -> str:
    digest = bytearray(hashlib.sha256(username.encode('utf-8')).digest())
    for i, v in enumerate(digest):
        digest[i] = SUBST[v]

    chars = []
    bit_buffer = 0
    bit_count = 0

    for byte in digest:
        bit_buffer = ((bit_buffer << 8) | byte) & 0xFFFFFFFF
        bit_count += 8
        while bit_count >= 5:
            bit_count -= 5
            chars.append(ALPHABET[(bit_buffer >> bit_count) & 0x1F])

    if bit_count > 0:
        chars.append(ALPHABET[((bit_buffer << (5 - bit_count)) & 0xFFFFFFFF) & 0x1F])

    return ''.join(chars)[:25]


def keygen(name: str) -> str:
    if len(name) < 6:
        raise ValueError('Username must be at least 6 characters long.')
    raw = _generate_raw(name)
    return '-'.join(raw[i:i+5] for i in range(0, 25, 5))


def verify(name: str, serial: str) -> bool:
    if len(name) < 6:
        return False
    normalized = serial.replace('-', '').upper()
    if len(normalized) != 25:
        return False
    expected = keygen(name).replace('-', '')
    return normalized == expected



